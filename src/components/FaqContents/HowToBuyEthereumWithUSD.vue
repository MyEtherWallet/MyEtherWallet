      <h3 id="now-the-fun-part-how-to-buy-eth">Now, the fun part! How To Buy ETH</h3>

      <ol>
        <li><p>Create an account on Gemini or Coinbase or Kraken.</p>
        </li>
        <li><p>Verify that account. You will need to upload a wide variety of documents proving you are who you say you are and stuff. It's all regulatory stuff. Once you upload this stuff, verification takes about a day or two (time is dependent on how busy the service is. During times of price increases and tons of new users, it may take longer. Be patient.)</p>
        </li>
        <li><p>Follow the instructions on the exchange to deposit USD into that exchange. This, depending on your bank and the wire transfer, will typically take 3-5 business days.</p>
        </li>
        <li><p>Now you have USD in your exchange account and can buy ETH with it.</p>
        </li>
        <li><p>Once you have all the ETH you want, withdraw that ETH into a wallet that you control. Exchanges are notorious for being hacked (although the ones mentioned above are very reputable) and you want your funds in a place that you have the private key to. So...</p>
        </li>
        <li><p>...While you are waiting for your verification and transfer, download and install <a href="https://github.com/ethereum/mist/releases">Mist / Ethereum Wallet</a>. Run it so it can sync the blockchain.</p>
        </li>
        <li><p>When you run it, it'll prompt you to make a new account. Create a password that you will never ever forget and create the account. Then, it'll give you an address that starts with 0x.. This what you will enter on your exchange to move your ETH from their account to your own.</p>
        </li>
        <li><p>To safely keep your account you have 3 pieces of information:</p>
          <ul>
            <li>Your address (0x7573aB.....)</li>
            <li>Your password</li>
            <li>Your private key</li>
          </ul>
        </li>
      </ol>

      <p>The private key is like a password, but way more intense. Plus, it's a password that is protected by another password. In Ethereum Wallet, this private key is in the form of a <code>keystore file</code>. You need to make a backup of this in case anything happens to your computer.</p>
      <p>In Ethereum Wallet, go to the top bar and find <code>ACCOUNTS</code> -&gt; <code>BACKUP</code> -&gt; <code>ACCOUNTS</code>. It'll open up a folder and inside you will see a file. This file is your private key. Copy this to a USB drive and safely store the USB drive in a different physical location than your computer (in case your house explodes).</p>
      <h3 id="alternative-steps-1-5-">Alternative steps 1-5:</h3>
      <p>If you already have BTC, you can use Shapeshift.io, Bity.com, or Changelly to convert your BTC into ETH. Start a BTC-&gt;ETH exchange and it'll tell you where to send your BTC. (If you have your BTC on an exchange, you can likely exchange them for ETH on that exchange without the need for Shapeshift.)</p>
      <p>Then create a Ethereum Wallet account and back it up. In Circle or Coinbase, send all the BTC to that address. The ETH will appear in your ETH wallet in ~20 minutes.</p>
