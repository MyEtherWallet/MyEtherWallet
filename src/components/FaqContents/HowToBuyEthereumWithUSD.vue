        regulatory stuff. Once you upload this stuff, verification takes about a day
        or two (time is dependent on how busy the service is. During times of price
        increases and tons of new users, it may take longer. Be patient.)
      </li>
      <li>
        Follow the instructions on the exchange to deposit USD into that exchange.
        This, depending on your bank and the wire transfer, will typically
        take 3-5 business days.
      </li>
      <li>
        Now you have USD in your exchange account and can buy ETH with it.
      </li>
      <li>
        Once you have all the ETH you want, withdraw that ETH into a wallet that you
        control. Exchanges are notorious for being hacked (although the ones mentioned
        above are very reputable) and you want your funds in a place that you
        have the private key to. So...
      </li>
      <li>
        ...While you are waiting for your verification and transfer, download and install
        <a href="https://github.com/ethereum/mist/releases">Mist / Ethereum Wallet</a>.
        Run it so it can sync the blockchain.
      </li>
      <li>
        When you run it, it'll prompt you to make a new account. Create a password
        that you will never ever forget and create the account. Then, it'll give you
        an address that starts with 0x.. This what you will enter on your exchange to
        move your ETH from their account to your own.
      </li>
      <li>
