// eslint-disable-next-line
const unit = require('ethjs-unit');

export default {
  components: {
    'dropdown-address-selector': DropDownAddressSelector,
    'interface-container-title': InterfaceContainerTitle,
    'interface-bottom-text': InterfaceBottomText,
    blockie: Blockie,
    'currency-picker': CurrencyPicker,
    'dropdown-coin-selector': DropDownCoinSelector
  },
  props: {
    tokensWithBalance: {
      type: Array,
      default: function() {
        return [];
      }
    }
  },
  data() {
    return {
      buttonSendTX: {
        title: this.$t('interface.sendTx'),
        buttonStyle: 'green',
        rightArrow: true,
        fullWidth: false,
        isThisMobileBottomButton: true // Hide bottom button automatically
      },
      addressSelector: {
        title: 'To Address',
        buttonCopy: true,
        buttonClear: true,
        popover: 'What is address'
      },
      coinSelector: {
        title: 'Type'
      },
      inputAmount: {
        title: 'Amount',
        value: '',
        type: 'text',
        buttonCopy: false,
        buttonClear: false,
        buttonCustom: 'Entire Balance',
        topTextInfo: '',
        popover: '',
        placeHolder: '',
        rightInputText: ''
      },
      inputData: {
        title: '',
        value: '',
        type: 'text',
        buttonCopy: false,
        buttonClear: false,
        buttonCustom: '',
        topTextInfo: '',
        popover: '',
        placeHolder: 'Add Data (e.g. 0x98049238502957203957)',
        rightInputText: ''
      },
      inputGasLimit: {
        title: '',
        value: '',
        type: 'text',
        buttonCopy: false,
        buttonClear: false,
        buttonCustom: '',
        topTextInfo: '',
        popover: '',
        placeHolder: 'Gas Limit',
        rightInputText: ''
      },
      advancedExpend: false,
      validAddress: true,
      amount: 0,
      amountValid: true,
      nonce: 0,
      gasLimit: 21000,
      data: '0x',
      gasAmount: this.$store.state.gasPrice,
      parsedBalance: 0,
      address: '',
      transactionFee: 0,
      selectedCurrency: { symbol: 'ETH', name: 'Ethereum' },
      raw: {},
      signedTx: '',
      resolvedAddress: ''
    };
  },
  computed: {
    ...mapGetters({
      account: 'account'
    })
  },
  watch: {
    address(newVal) {
      this.address = newVal;
      if (this.verifyAddr()) {
        this.validAddress = false;
      } else {
        this.estimateGas();
        this.validAddress = true;
      }
    },
    parsedBalance(newVal) {
      this.parsedBalance = newVal;
    },
    gasAmount(newVal) {
      this.gasAmount = newVal;
      if (!this.verifyAddr()) {
        this.estimateGas();
      }
      this.$store.dispatch('setGasPrice', Number(newVal));
    },
    amount(newVal) {
      this.amount = newVal;
      if (!this.verifyAddr()) {
        this.estimateGas();
      }
    },
    selectedCurrency(newVal) {
      this.estimateGas();
      this.selectedCurrency = newVal;
    }
  },
  mounted() {
    if (this.account.balance) {
      this.parsedBalance = unit.fromWei(
        parseInt(this.account.balance),
        'ether'
      );
    }
  },
  methods: {
    copyToClipboard(ref) {
      this.$refs[ref].select();
      document.execCommand('copy');
    },
    async createTx() {
      const jsonInterface = [
        {
          constant: false,
          inputs: [
            { name: '_to', type: 'address' },
            { name: '_amount', type: 'uint256' }
          ],
          name: 'transfer',
          outputs: [{ name: 'success', type: 'bool' }],
          payable: false,
          type: 'function'
        }
      ];
      const contract = new this.$store.state.web3.eth.Contract(jsonInterface);
      const isEth = this.selectedCurrency.name === 'Ethereum';
      this.nonce = await this.$store.state.web3.eth.getTransactionCount(
        this.$store.state.wallet.getAddressString()
      );
      this.data = isEth
        ? this.data
        : contract.methods
            .transfer(this.address, unit.toWei(this.amount, 'ether'))
            .encodeABI();

      this.raw = {
        from: this.$store.state.wallet.getAddressString(),
        gas: this.gasLimit,
        nonce: this.nonce,
        gasPrice: Number(unit.toWei(this.$store.state.gasPrice, 'gwei')),
        value: isEth
          ? this.amount === ''
            ? 0
            : unit.toWei(this.amount, 'ether')
          : 0,
        to: isEth ? this.address : this.selectedCurrency.addr,
        data: this.data,
        chainId: this.$store.state.network.type.chainID || 1
      };

      if (this.address === '') {
        delete this.raw['to'];
      }

      const fromAddress = this.raw.from;
      this.$store.state.web3.eth
        .sendTransaction(this.raw)
        .once('transactionHash', hash => {
          this.$store.dispatch('addNotification', [
            fromAddress,
            hash,
            'Transaction Hash'
          ]);
        })
        .on('receipt', res => {
          this.$store.dispatch('addNotification', [
            fromAddress,
            res,
            'Transaction Receipt'
          ]);
        })
        .on('error', err => {
          this.$store.dispatch('addNotification', [
            fromAddress,
            err,
            'Transaction Error'
          ]);
        });
    },
    confirmationModalOpen() {
      this.createTx();
      window.scrollTo(0, 0);
    },
    changeGas(val) {
      this.gasAmount = val;
      this.createDataHex();
      this.$store.dispatch('setGasPrice', Number(val));
    },
    setBalanceToAmt() {
      if (this.selectedCurrency.name === 'Ethereum') {
        this.amount = this.parsedBalance - this.transactionFee;
      }
    },
    createDataHex() {
      if (this.selectedCurrency.name !== 'Ethereum') {
        const jsonInterface = [
          {
            constant: false,
            inputs: [
              { name: '_to', type: 'address' },
              { name: '_amount', type: 'uint256' }
            ],
            name: 'transfer',
            outputs: [{ name: 'success', type: 'bool' }],
            payable: false,
            type: 'function'
          }
        ];
        const contract = new this.$store.state.web3.eth.Contract(jsonInterface);
        this.data = contract.methods
          .transfer(this.address, this.amount)
          .encodeABI();
      } else {
        this.data = '0x';
      }
    },
    setSelectedCurrency(e) {
      this.selectedCurrency = e;
      this.createDataHex();
    },
    estimateGas() {
      const newRaw = this.raw;
      delete newRaw['gas'];
      delete newRaw['nonce'];
      this.createDataHex();
      this.$store.state.web3.eth
        .estimateGas(newRaw)
        .then(res => {
          this.transactionFee = unit.fromWei(
            unit.toWei(this.$store.state.gasPrice, 'gwei') * res,
            'ether'
          );
          this.gasLimit = res;
        })
        .catch(err => {
          // eslint-disable-next-line no-console
          console.error(err);
        });
    },
    verifyAddr() {
      if (this.address.length !== 0 && this.address !== '') {
        const valid = this.$store.state.web3.utils.isAddress(this.address);
        if (!valid) {
          return true;
        }
        return false;
      }
    }
  }
};
</script>

